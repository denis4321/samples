package org.datastructures.list.impl.linkedlist;

/**
 *
 * @author prokopiukd
 */
public class Node {

    private Object value;
    private Node next;
    private Node prev;

    public Object getValue() {
        return value;
    }

    public void setValue(Object value) {
        this.value = value;
    }

    public Node getNext() {
        return next;
    }

    public void setNext(Node next) {
        this.next = next;
    }

    public Node getPrev() {
        return prev;
    }

    public void setPrev(Node prev) {
        this.prev = prev;
    }

}
