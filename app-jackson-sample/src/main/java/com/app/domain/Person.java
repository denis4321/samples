package com.app.domain;

import com.fasterxml.jackson.annotation.JsonIgnore;

public class Person {
    @JsonIgnore
    private String name;
    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
